# --- 2. PDF ENGINE ---
class SchoolPDF(FPDF):
    def header(self):
        # Background color for header
        self.set_fill_color(230, 230, 230)
        self.set_font('Arial', 'B', 16)
        
        # Institution Name
        school_name = st.session_state.data_store.get("School_Name", "EDUCATIONAL INSTITUTION").upper()
        self.cell(0, 15, school_name, 0, 1, 'C', fill=True)
        
        # Report Title
        self.set_font('Arial', 'I', 10)
        self.cell(0, 10, "Official Performance & Efficiency Report", 0, 1, 'C')
        self.ln(5)

    def footer(self):
        self.set_y(-15)
        self.set_font('Arial', 'I', 8)
        self.cell(0, 10, f'Page {self.page_no()} | Generated by AI System 2026', 0, 0, 'C')

def create_pdf(data, title):
    pdf = SchoolPDF()
    pdf.add_page()
    df = pd.DataFrame(data)
    
    # Report Section Title
    pdf.set_font('Arial', 'B', 12)
    pdf.cell(0, 10, f"Section: {title}", 0, 1, 'L')
    pdf.ln(2)
    
    # Table Header
    pdf.set_font('Arial', 'B', 10)
    pdf.set_fill_color(200, 220, 255)
    
    if not df.empty:
        col_width = 190 / len(df.columns)
        for col in df.columns:
            pdf.cell(col_width, 10, str(col), 1, 0, 'C', fill=True)
        pdf.ln()
        
        # Table Body
        pdf.set_font('Arial', '', 9)
        for _, row in df.iterrows():
            for val in row:
                pdf.cell(col_width, 10, str(val), 1, 0, 'C')
            pdf.ln()
            
    return pdf.output(dest='S').encode('latin-1')

# --- 3. BULK UPLOAD LOGIC ---
def handle_bulk_upload():
    st.sidebar.markdown("---")
    st.sidebar.subheader("üìÇ Excel Bulk Load")
    upload_type = st.sidebar.selectbox("Select Upload Type", ["Classes", "Student Performance", "Teachers"])
    uploaded_file = st.sidebar.file_uploader(f"Upload {upload_type} Excel", type=["xlsx"])

    if uploaded_file:
        try:
            df = pd.read_excel(uploaded_file)
            df.columns = [str(c).strip() for c in df.columns]
            
            if st.sidebar.button(f"Process {upload_type}"):
                if upload_type == "Classes":
                    for _, row in df.iterrows():
                        key = f"{row['Grade']}-{row['Section']}"
                        subs = [s.strip() for s in str(row['Subjects']).split(",")]
                        st.session_state.data_store["Grades_Config"][key] = subs
                    st.sidebar.success("Classes Loaded!")
                
                elif upload_type == "Student Performance":
                    for col in ['A', 'B', 'C', 'D']:
                        df[col] = pd.to_numeric(df[col], errors='coerce').fillna(0)
                    for _, row in df.iterrows():
                        st.session_state.data_store["A"].append({
                            "Class": str(row['Class']), "Subject": str(row['Subject']),
                            "A": int(row['A']), "B": int(row['B']), "C": int(row['C']), "D": int(row['D']),
                            "Total": int(row['A']+row['B']+row['C']+row['D'])
                        })
                    st.sidebar.success(f"Loaded {len(df)} Records!")

                elif upload_type == "Teachers":
                    for _, row in df.iterrows():
                        st.session_state.data_store["B"].append({
                            "Name": row['Name'], "Expertise": row['Expertise'], "Success": row['Success']
                        })
                    st.sidebar.success(f"Loaded {len(df)} Teachers!")
                st.rerun()
        except Exception as e:
            st.sidebar.error(f"Error processing file.")

